        <h2>L’ossature bois</h2>
        <p>Le procédé de fabrication consiste à clouer un bardage sur une ossature en bois (sections différentes en fonction de la grandeur de la construction).<br />
        Cette technique permet à chaque lame de bardage de travailler librement sans risque de déformation.<br />
        Le principe utilisé est le même que pour les maisons à ossature bois.</p>
        
        <p>Le bardage (planches extérieures de la construction) d’une épaisseur de 21 mm est en sapin scandinave. Les rainures au dos de chaque lame permettent d’éviter au bois de tuiler.</p>
        
        <p>La jonction des panneaux sont protégés par un couvre joint et les angles par des cornières, ainsi aucun assemblage n’est visible.<br />
        Les murs extérieurs sont lasurés en ateliers. Nous vous conseillons d’appliquer deux couches supplémentaires pour une protection optimale des bois avec un suivi tous les 2 à 6 ans  suivant  la qualité du produit appliqué.</p>
	</div>
    
