<h2>Charpente</h2>
<p>La charpente est en sapin scandinave. Les pentes de toit et les sections de bois utilisées, dépendent de la couverture choisie et des contraintes d’urbanisme.<br />
La couverture de base est en bardeaux d’asphalte ( shingle ) de couleur tuile ou gris ardoise avec une finition du faitage en zinc.<br />
Les sous pentes de toit en voliges ( sapin scandinave ) de 16 mm d’épaisseur sont visibles de l’intérieur.<br />
Pour les structures type appentis, charreteries, bûchers les poteaux en sapin sont en 145x145 rabotés et chanfreinés.</p>

