        <h2>Dalle béton</h2>
        <p>Nous vous conseillons de poser votre abri sur une dalle béton (que nous pouvons réaliser si vous le souhaitez).<br />Celle-ci doit correspondre aux dimensions exactes de votre construction.<br />L’étanchéité est assurée par le recouvrement du bardage sur la dalle.</p>
        
