<h1>Entreprise créée en 1973, Coraloisirs réalise sur mesure des constructions en sapin scandinave : Abris de jardin, garages, appentis, préaux, bûchers, kiosques, pergolas, chalets de loisirs, abris bus etc.</h1>

<p>Situé à Saint Denis en Val dans le Loiret, à 5 km au sud d’Orléans, Coraloisirs vous accueille sur 2000 m2.<br />
Notre atelier de fabrication ainsi que nos bureaux se trouvent à côté de notre exposition permanente où vous pourrez voir et comparez quelques modèles de fabrication.</p>

<p>Vos principaux interlocuteurs seront Monsieur Gorget, gérant de l’entreprise ou Madame Gorget son épouse.<br />
Ils vous conseilleront sur le choix du modèle, vous donneront les conseils techniques et vous proposeront le chiffrage de votre projet.</p>

<p>La fabrication sur mesure vous permettra d’avoir une construction personnalisée, à la fois dans le choix des dimensions mais aussi des options et des couleurs.<br />
De la conception à la réalisation, nous maitrisons toutes les étapes de fabrication de votre projet.</p>

<h2>Notre force le « sur mesure ».</h2>

